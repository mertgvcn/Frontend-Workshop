class myMath {

    constructor() {}

    static topla(a,b) { //class'a özgüdür, biliyorsun
        console.log(a+b);
    }

    static cikar(a,b) {
        console.log(a-b);
    }

    static carp(a,b) {
        console.log(a*b);
    }

    static bol(a,b) {
        console.log(a/b);
    }

}

myMath.topla(2,3); 
myMath.carp(2,3);